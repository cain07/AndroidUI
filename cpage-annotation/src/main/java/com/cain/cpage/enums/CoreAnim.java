package com.cain.cpage.enums;
/**
  * @Description:    页面切换动画枚举
  * @Author:         cain
  * @CreateDate:     2020/5/31 5:49 PM
  * @Version:        1.0
 */
public enum CoreAnim {
    /**
     * 没有动画
     */
    none,

    /**
     * 由下到上动画
     */
    present,

    /**
     * 从左到右动画
     */
    slide,

    /**
     * 渐变
     */
    fade,

    /**
     * 放大
     */
    zoom
}
